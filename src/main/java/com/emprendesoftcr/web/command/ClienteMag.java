package com.emprendesoftcr.web.command;


public class ClienteMag {
	private String indicadorActivoMAG;
	private String estadoMAG;
	private String fechaAltaMAG;
	private String nombreMAG;
	private String fechaBajaMAG;
	
	public ClienteMag() {
		super();
	}

	
	
	
	public String getIndicadorActivoMAG() {
		return indicadorActivoMAG;
	}



	
	public void setIndicadorActivoMAG(String indicadorActivoMAG) {
		this.indicadorActivoMAG = indicadorActivoMAG;
	}



	public String getEstadoMAG() {
		return estadoMAG;
	}
	
	public void setEstadoMAG(String estadoMAG) {
		this.estadoMAG = estadoMAG;
	}
	
	public String getFechaAltaMAG() {
		return fechaAltaMAG;
	}
	
	public void setFechaAltaMAG(String fechaAltaMAG) {
		this.fechaAltaMAG = fechaAltaMAG;
	}
	
	public String getNombreMAG() {
		return nombreMAG;
	}
	
	public void setNombreMAG(String nombreMAG) {
		this.nombreMAG = nombreMAG;
	}
	
	public String getFechaBajaMAG() {
		return fechaBajaMAG;
	}
	
	public void setFechaBajaMAG(String fechaBajaMAG) {
		this.fechaBajaMAG = fechaBajaMAG;
	}
	
	

}
